class Mundo1
{
private:
    Jugador* jugador;
    vector<Obstaculo*> Rocas;
    vector<Recurso*> mineros;
    int metaX, contador = 0;
    int posX, posY;
    int (*mapa1)[120];
    bool nivel1Completado, nivel2Completado;
public:
    Mundo1(Jugador*& jugador, int posX, int posY) {
        this->jugador = jugador;
        this->posX = posX;
        this->posY = posY;
        this->metaX = 114;
        this->mapa1 = nivel1parte1;
        this->nivel1Completado = false;
        this->nivel2Completado = false;
    }
    void dibujarMapa() {
        Console::ForegroundColor = ConsoleColor::DarkYellow;
        for (int i = 0; i < 25; i++)
        {
            for (int j = 0; j < 120; j++)
            {
                Console::SetCursorPosition(j, i);
                switch (mapa1[i][j]) {
                case 1: cout << (char)219; break;
                case 2: cout << (char)95; break;
                case 3: cout << (char)124; break;
                case 4: cout << (char)186; break;
                }
            }
        }
        Console::ResetColor();
    }
    void generarRocas() {
        // 5 - 20 a 5 - 53
        contador++;
        if (contador % 15 == 0)
        {
            for (int i = 0; i < 5; i++)
            {
                Obstaculo* o = new Roca(20 + rand() % 34, 6);
                Rocas.push_back(o);
            }
        }
    }
    void generarMineros() {
        // 13 - 17 a 19 - 47
        for (int i = 0; i < 5; i++)
        {
            Recurso* o = new Recurso(17 + rand() % 29, 13 + rand() % 7, " O ", "/|\\", "7 \\");
            mineros.push_back(o);
        }
        // 15 - 61 a 18 - 103
        for (int i = 0; i < 25; i++)
        {
            Recurso* o = new Recurso(61 + rand() % 43, 15 + rand() % 4, " O ", "/|\\", "7 \\");
            mineros.push_back(o);
        }
    }
    void dibujarMineros() {
        for (int i = 0; i < mineros.size(); i++)
        {
            mineros[i]->dibujar();
        }
    }
    void dibujarRocas() {
        for (int i = 0; i < Rocas.size(); i++)
        {
            Rocas[i]->mover(mapa1);
        }
    }
    void colisiones() {
        Rectangle rectJugador = jugador->getRectangle();
        for (int i = 0; i < Rocas.size(); i++)
        {
            if (Rocas[i]->isActivo())
            {
                Rectangle rectEnemigo = Rocas[i]->getRectangle();
                if (rectJugador.IntersectsWith(rectEnemigo))
                {
                    jugador->perderMineros();
                    jugador->borrar();
                    jugador->setX(posX);
                    jugador->setY(posY);
                }
            }
        }
        for (int j = 0; j < mineros.size(); j++)
        {
            if (mineros[j]->isActivo())
            {
                Rectangle rectMinero = mineros[j]->getRectangle();
                if (rectJugador.IntersectsWith(rectMinero))
                {
                    jugador->aumentarMineros();
                    mineros[j]->borrar();
                    mineros.erase(mineros.begin() + j);
                }
            }
        }
    }
    void dibujarInfo() {
        Console::SetCursorPosition(1, 27);
        cout << "Numero de mineros vivos: " << jugador->getMineros() << endl;
        cout << "Mision: Rescata a los mineros que no signifiquen un peligro, la seguridad de muchos esta sobre la de pocos...";
    }
    void juegoParte1() {
        dibujarMapa();
        generarMineros();
        while (jugador->getVivo() || !nivel1Completado) {
            dibujarInfo();
            generarRocas();
            dibujarRocas();
            dibujarMineros();
            if (_kbhit())
            {
                char tecla = _getch();
                jugador->mover(tecla, mapa1);
            }
            colisiones();
            if (jugador->getMineros() <= 0)
            {
                jugador->setVivo(false);
                Console::Clear();
                cout << "FIN DEL JUEGO";
                break;
            }
            if (jugador->getMineros() >= 2 && jugador->getX() + 3 >= metaX) {
                nivel1Completado = true;
                Console::Clear();
                cout << "HAZ PASADO EL NIVEL 1";
                break;
            }
            _sleep(100);
        }
    }
    void juegoParte2() {

    }
};

